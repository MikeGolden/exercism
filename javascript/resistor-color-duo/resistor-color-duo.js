export const decodedValue = (colors) => {
  const colorMap = [
    'black',
    'brown',
    'red',
    'orange',
    'yellow',
    'green',
    'blue',
    'violet',
    'grey',
    'white',
  ];

  return colorMap.indexOf(colors[0]) * 10 + colorMap.indexOf(colors[1]);
};
